#Boolean
ya = True
ga = False

print(ya)
print(ga)


#Logika AND
print("Logika AND")
print(ya and ya) #true
print(ya and ga) #false
print(ga and ya) #false
print(ga and ga) #false

#Logika OR
print("Logika OR")
print(ya or ya) #true
print(ya or ga) #true
print(ga or ya) #true
print(ga or ga) #false


#Perbandingan
print("Perbandingan")
a = 2
b = 4

print(a > b) # 2 > 4 false
print(a < b) # 2 < 4 true
print(a >= b) # 2 >= 4 false
print(a <= b) # 2 <= 4 true
print(a == b) # 2 == 4 false
print(a != b) # 2 != 4 true

